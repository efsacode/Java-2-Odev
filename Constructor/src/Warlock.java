
public class Warlock {
	private String name;
	private String powerType;
	private int power;
	private int Health;
	
	public String getName() {
		return name;
	}
	public String getPowerType() {
		return powerType;
	}
	public int getPower() {
		return power;
	}

	public int getHealth() {
		return Health;
	}
	
	public Warlock(String name, String powerType, int power, int health) {
		super();
		this.name = name;
		this.powerType = powerType;
		this.power = power;
		Health = health;
	}
}
